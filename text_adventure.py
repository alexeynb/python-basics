# Простое текстовое приключение

def greet_user():
    """Функция приветствия"""
    print("Добро пожаловать в текстовое приключение!")
    name = input("Как вас зовут? ")
    print(f"Приятно познакомиться, {name}!")
    return name

def choose_path():
    """Функция выбора пути"""
    print("\nВы находитесь перед двумя дверями.")
    print("1. Синяя дверь")
    print("2. Красная дверь")
    choice = input("Какую дверь вы выберете? (1/2): ")
    return choice

def blue_door():
    """События за синей дверью"""
    print("\nВы вошли в синюю дверь и увидели сундук.")
    print("В сундуке лежит сокровище!")
    print("Поздравляем, вы победили!")

def red_door():
    """События за красной дверью"""
    print("\nВы вошли в красную дверь и встретили дракона.")
    action = input("Будете драться или убегать? (драться/убегать): ").lower()
    if action == "драться":
        print("Вы победили дракона! Поздравляем!")
    else:
        print("Вы убежали. Игра окончена.")

def main():
    """Основная функция программы"""
    name = greet_user()
    while True:
        choice = choose_path()
        if choice == "1":
            blue_door()
            break
        elif choice == "2":
            red_door()
            break
        else:
            print("Неверный выбор. Попробуйте снова.")
    print(f"\nСпасибо за игру, {name}!")

# Запуск программы
if __name__ == "__main__":
    main()
